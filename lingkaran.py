print('==============================')
r = int(input('masukan jari-jari lingkaran: '))
phi = 3.14
L = phi * (r * r)
K = 2 * phi * r
print('Luas lingakaran dengan jari-jari {} adalah {}'.format(r, L))
print('keliling lingakaran dengan jari-jari {} adalah {}'.format(r, K))

